    def get_objection_types(self) -> List[str]:
        """Get list of available objection types"""
        return list(self.OBJECTION_LIBRARY.keys())
    
    def handle_objection(self, objection_type: str, variant: str = "all") -> Dict:
        """Get response for specific objection type"""
        if objection_type not in self.OBJECTION_LIBRARY:
            return {
                "error": f"Unknown objection type. Available: {self.get_objection_types()}"
            }
        
        objection = self.OBJECTION_LIBRARY[objection_type]
        
        if variant == "all":
            return {
                "type": objection_type,
                "name": objection["name"],
                "common_phrases": objection["common_phrases"],
                "responses": objection["responses"],
                "recommendation": self._get_recommendation(objection_type)
            }
        elif variant in objection["responses"]:
            return {
                "type": objection_type,
                "name": objection["name"],
                "variant": variant,
                "response": objection["responses"][variant]
            }
        else:
            return {
                "error": f"Unknown variant. Use: soft, confident, expert, or all"
            }
    
    def _get_recommendation(self, objection_type: str) -> str:
        """Get recommendation on which variant to use"""
        recommendations = {
            "price": "Используйте EXPERT для аналитиков, CONFIDENT для тех, кто торгуется, SOFT для вежливых сомнений",
            "time": "Используйте EXPERT для хаоса в приоритетах, CONFIDENT для привычки откладывать, SOFT для перегруженных",
            "fear": "Используйте EXPERT для управления рисками, CONFIDENT для нужды в гарантиях, SOFT для тревожных",
            "competitors": "Используйте EXPERT для табличного сравнения, CONFIDENT для обоснования премиума, SOFT для избежания конфликта",
            "trust": "Используйте EXPERT для профессионалов, CONFIDENT для скептиков, SOFT для осторожных"
        }
        return recommendations.get(objection_type, "Выбирайте по типу клиента")
    
    def detect_objection(self, client_message: str) -> List[str]:
        """Detect objection type from client message"""
        message_lower = client_message.lower()
        detected = []
        
        keywords = {
            "price": ["дорого", "дешевле", "скидка", "бюджет", "рассрочка", "цена", "стоимость", "деньги"],
            "time": ["потом", "позже", "некогда", "через месяц", "не сейчас", "время", "когда будет", "отложим"],
            "fear": ["боюсь", "не получится", "не уверен", "риск", "сложно", "не разбираюсь", "а вдруг"],
            "competitors": ["конкурент", "другой", "нашёл", "почему вы", "крупные клиенты", "у них"],
            "trust": ["доверие", "кто вы", "кейсы", "отзывы", "сертификат", "гарантия", "портфолио"]
        }
        
        for obj_type, words in keywords.items():
            if any(word in message_lower for word in words):
                detected.append(obj_type)
        
        return detected if detected else ["unknown"]
    
